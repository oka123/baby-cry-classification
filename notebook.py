"""---
## 6. Data Preprocessing

Tahapan: **Resampling → Normalisasi → Trimming Silence → Segmentasi (2 detik, khusus infant cry) → Augmentasi (khusus train)**.

Segmentasi 2 detik berfungsi ganda: menyeragamkan panjang audio (dibutuhkan CNN/mel-spectrogram) sekaligus menambah jumlah data (satu file panjang bisa menghasilkan beberapa segmen).
"""

def load_and_preprocess(filepath, sr=SAMPLE_RATE):
    """Resampling + normalisasi + trimming silence."""
    y, _ = librosa.load(filepath, sr=sr)          # resampling ke target SR
    y, _ = librosa.effects.trim(y, top_db=25)       # trimming silence
    if np.max(np.abs(y)) > 0:
        y = y / np.max(np.abs(y))                   # normalisasi amplitudo ke [-1, 1]
    return y

def segment_audio(y, segment_length=SEGMENT_LENGTH, sr=SAMPLE_RATE):
    """Segmentasi audio jadi window tetap (2 detik).
    Audio lebih pendek dari 2 detik di-pad (zero padding).
    Audio lebih panjang dipecah jadi beberapa segmen non-overlap.
    """
    segments = []
    if len(y) <= segment_length:
        padded = np.pad(y, (0, segment_length - len(y)), mode='constant')
        segments.append(padded)
    else:
        n_segments = len(y) // segment_length
        for i in range(n_segments):
            segments.append(y[i*segment_length:(i+1)*segment_length])
        remainder = len(y) - n_segments * segment_length
        if remainder > segment_length * 0.5:  # sisa signifikan -> pad jadi 1 segmen tambahan
            last = y[n_segments*segment_length:]
            last = np.pad(last, (0, segment_length - len(last)), mode='constant')
            segments.append(last)
    return segments

