"""---
## 7. Ekstraksi Fitur

**Untuk ML Tradisional (SVM, Random Forest):**
- Domain waktu: RMS Energy, Zero Crossing Rate (ZCR)
- Domain frekuensi: Spectral Centroid, Spectral Bandwidth
- Representasi tambahan: Pitch (F0) via `librosa.pyin`
- Representasi utama: MFCC (13 koefisien)

**Untuk ML Modern (CNN, CNN-LSTM):**
- Mel-Spectrogram (2D time-frequency representation)

### 7.1 Fungsi Ekstraksi Fitur (Domain Waktu, Frekuensi, MFCC, Pitch)
"""

N_MFCC = 13

def extract_features_traditional(y, sr=SAMPLE_RATE, n_mfcc=N_MFCC):
    """Ekstrak seluruh fitur frame-level untuk satu segmen audio.
    Return dict berisi array time-series tiap fitur (sebelum feature engineering)."""
    # Domain waktu
    rms = librosa.feature.rms(y=y)[0]
    zcr = librosa.feature.zero_crossing_rate(y=y)[0]

    # Domain frekuensi
    spectral_centroid = librosa.feature.spectral_centroid(y=y, sr=sr)[0]
    spectral_bandwidth = librosa.feature.spectral_bandwidth(y=y, sr=sr)[0]

    # Pitch (F0) - pyin lebih robust untuk suara manusia/bayi dibanding piptrack
    f0, voiced_flag, voiced_prob = librosa.pyin(
        y, fmin=librosa.note_to_hz('C2'), fmax=librosa.note_to_hz('C7'), sr=sr
    )
    f0 = f0[~np.isnan(f0)]
    if len(f0) == 0:
        f0 = np.array([0.0])

    # MFCC (representasi utama)
    mfcc = librosa.feature.mfcc(y=y, sr=sr, n_mfcc=n_mfcc)

    return {
        'rms': rms, 'zcr': zcr,
        'spectral_centroid': spectral_centroid, 'spectral_bandwidth': spectral_bandwidth,
        'pitch': f0, 'mfcc': mfcc
    }

"""### 7.2 Feature Engineering (Wajib untuk ML Tradisional)

Setiap fitur time-series (RMS, ZCR, Spectral Centroid, Spectral Bandwidth, Pitch, dan tiap koefisien MFCC) diringkas menjadi 4 statistik: **mean, std, min, max**.

Untuk perbandingan **MFCC vs MFCC+Pitch**, disiapkan dua vektor fitur berbeda.
"""

def summarize_stats(arr, prefix):
    """Ringkas array 1D jadi mean, std, min, max."""
    return {
        f'{prefix}_mean': np.mean(arr), f'{prefix}_std': np.std(arr),
        f'{prefix}_min': np.min(arr), f'{prefix}_max': np.max(arr)
    }

def build_feature_vector(feat_dict, include_pitch=True):
    """Bangun satu baris feature vector (flat) dari feat_dict hasil extract_features_traditional."""
    row = {}
    row.update(summarize_stats(feat_dict['rms'], 'rms'))
    row.update(summarize_stats(feat_dict['zcr'], 'zcr'))
    row.update(summarize_stats(feat_dict['spectral_centroid'], 'centroid'))
    row.update(summarize_stats(feat_dict['spectral_bandwidth'], 'bandwidth'))
    for i in range(feat_dict['mfcc'].shape[0]):
        row.update(summarize_stats(feat_dict['mfcc'][i], f'mfcc{i+1}'))
    if include_pitch:
        row.update(summarize_stats(feat_dict['pitch'], 'pitch'))
    return row

def build_feature_dataframe(segments, labels, include_pitch=True):
    rows = []
    for y in segments:
        feat_dict = extract_features_traditional(y)
        rows.append(build_feature_vector(feat_dict, include_pitch=include_pitch))
    df_feat = pd.DataFrame(rows)
    df_feat['label'] = labels
    return df_feat

